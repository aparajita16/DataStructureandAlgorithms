class Node:
    def __init__(self, data):
        self.data = data
        self.next = None

class LinkedList:
    def __init__(self):
        self.head = None

    def append(self, data):
        new_node = Node(data)
        if not self.head:
            self.head = new_node
        else:
            current = self.head
            while current.next:
                current = current.next
            current.next = new_node

    def remove_duplicates(self):
        current = self.head
        while current and current.next:
            if current.data == current.next.data:
                current.next = current.next.next
            else:
                current = current.next

    def print_list(self):
        current = self.head
        while current:
            print(current.data, end=" -> ")
            current = current.next
        print("None")
ll = LinkedList()
elements = list(map(int, input("Enter the sorted linked list elements separated by space: ").split()))
for elem in elements:
    ll.append(elem)

print("Original Linked List:")
ll.print_list()

ll.remove_duplicates()
print("Linked List after removing duplicates:")
ll.print_list()
